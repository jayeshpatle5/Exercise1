public class Question36 {
//	36.	Difference between iterator n enumerators? 

//	Iterator
	
//*	Iterator is a universal cursor as it is applicable 
//	for all the collection classes.
//	
//*	Iterator has the remove() method.
//	
//*	Iterator can do modifications (e.g using remove()
//	method it removes the element from the Collection 
//	during traversal).
	
//*	Iterator is not a legacy interface. 
//	Iterator can be used for the traversal 
//	of HashMap, LinkedList, ArrayList, HashSet.
	
//	Enumeration
	
//	*Enumeration is not a universal cursor as it applies only to legacy classes.
	
//	*Enumeration does not have the remove() method.	
	
//	Enumeration interface acts as a read only interface, 
//	one can not do any modifications to Collection while
//		traversing the elements of the Collection.
	
//	Enumeration is a legacy interface which is used for traversing Vector, Hashtable.
	
}
